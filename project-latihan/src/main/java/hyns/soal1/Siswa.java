package hyns.soal1;

public class Siswa {

    private String nrp;

    private String nama;

    private Nilai daftarNilai;

    public String getNrp() {
        return nrp;
    }

    public void setNrp(String nrp) {
        this.nrp = nrp;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public Nilai getDaftarNilai() {
        return daftarNilai;
    }

    public void setDaftarNilai(Nilai daftarNilai) {
        this.daftarNilai = daftarNilai;
    }
}
